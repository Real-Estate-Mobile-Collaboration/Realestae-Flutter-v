            const SizedBox(height: 16),
            _buildStatCard(
              'Favorites',
              '8',
              Icons.favorite_outline,
              Colors.red,
            ),
            const SizedBox(height: 16),
            _buildStatCard(
              'Messages Sent',
              '15',
              Icons.message_outlined,
