            ),
            const SizedBox(height: 12),
            _buildPaymentCard(
              'PayPal',
              'user@example.com',
              Icons.payment,
              false,
            ),
            const SizedBox(height: 24),
            SizedBox(
              width: double.infinity,
              height: 56,
              child: ElevatedButton.icon(
                onPressed: () {},
                icon: const Icon(Icons.add),
                label: const Text('Add Payment Method'),
                style: ElevatedButton.styleFrom(
